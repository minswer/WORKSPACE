/*
특징
부호가 없다:

기본 정수형(int)은 부호가 있는 자료형으로 양수와 음수를 모두 표현합니다.
unsigned는 양수만 표현하며, 음수는 사용할 수 없습니다.
값의 범위가 달라진다:

부호가 없기 때문에 모든 비트를 값 표현에 사용하여, 양수의 범위가 두 배로 늘어납니다.
예를 들어, 32비트 시스템에서는:
int: -2,147,483,648 ~ 2,147,483,647
unsigned int: 0 ~ 4,294,967,295
데이터 타입과 함께 사용:

unsigned는 주로 int와 함께 사용되지만, 다른 정수형 데이터 타입(short, long, char)에도 적용 가능합니다.
*/