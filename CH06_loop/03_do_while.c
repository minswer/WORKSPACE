// do-while 문문

/*
do-while 문은 C 언어에서 사용되는 반복 제어 구조로, 조건을 확인하기 전에 블록을 먼저 실행하는 루프입니다. 
즉, 조건이 거짓이더라도 블록이 최소 한 번은 실행된다는 특징이 있습니다.

    기본 구조
    c
    코드 복사
    do {
    // 실행할 코드
    } while (조건);

do 블록: 반복할 코드를 포함합니다.
while 조건: 루프를 계속 실행할지 여부를 결정하는 조건식입니다. 조건이 true이면 블록을 반복 실행하고, false이면 루프를 종료합니다.

마지막에 세미콜론(;)이 필요합니다.
작동 방식
do 블록 내부의 코드를 먼저 실행합니다.
실행 후, while의 조건식을 평가합니다.
조건식이 true면 다시 블록을 실행하고, false면 루프를 종료합니다.
예제 1: 기본 사용
c
코드 복사
#include <stdio.h>

int main() {
    int i = 1;

    do {
        printf("i의 값: %d\n", i);
        i++;
    } while (i <= 5);

    return 0;
}
출력:

less
코드 복사
i의 값: 1
i의 값: 2
i의 값: 3
i의 값: 4
i의 값: 5
예제 2: 조건이 처음부터 거짓일 때
c
코드 복사
#include <stdio.h>

int main() {
    int i = 10;

    do {
        printf("이 코드는 한 번은 실행됩니다.\n");
    } while (i < 5);

    return 0;
}
출력:

코드 복사
이 코드는 한 번은 실행됩니다.
조건 i < 5는 거짓이지만, do 블록이 조건 확인 전에 실행되므로 한 번 출력됩니다.
특징
최소 1회 실행 보장
do-while은 조건이 거짓이어도 코드 블록이 한 번은 실행됩니다.
일반 while 루프와의 차이점입니다.
조건 확인 위치
do-while: 블록 실행 후 조건 확인
while: 조건 확인 후 블록 실행
예제 3: 사용자 입력 받기
c
코드 복사
#include <stdio.h>

int main() {
    int num;

    do {
        printf("양수를 입력하세요: ");
        scanf("%d", &num);
    } while (num <= 0);

    printf("입력한 양수: %d\n", num);
    return 0;
}
사용자가 양수를 입력할 때까지 반복 실행됩니다.
장점
한 번 실행이 반드시 필요한 경우 적합합니다.
사용자 입력 확인이나 초기화 작업과 같은 특정 조건을 만족할 때까지 반복이 필요한 경우 유용합니다.
주의점
조건이 항상 true일 경우 무한 루프가 발생할 수 있으므로, 조건식과 루프 탈출 조건을 적절히 설계해야 합니다.
c
코드 복사
do {
    // 실행 코드
} while (1); // 무한 루프
이 경우 break 문을 사용해 루프를 종료해야 합니다.

do-while 문은 간단하지만 효과적인 반복문으로, 조건 확인 전 반드시 실행이 필요한 작업에 적합합니다!

*/